import { useState } from "react";
import Avatar from "@material-ui/core/Avatar";
import Button from "@material-ui/core/Button";
import CssBaseline from "@material-ui/core/CssBaseline";
import TextField from "@material-ui/core/TextField";
import FormControlLabel from "@material-ui/core/FormControlLabel";
import Checkbox from "@material-ui/core/Checkbox";
import Link from "@material-ui/core/Link";
import Grid from "@material-ui/core/Grid";
import Box from "@material-ui/core/Box";
import LockOutlinedIcon from "@material-ui/icons/LockOutlined";
import Typography from "@material-ui/core/Typography";
import { makeStyles } from "@material-ui/core/styles";
import Container from "@material-ui/core/Container";

function Copyright() {
  return (
    <Typography variant="body2" color="textSecondary" align="center">
      {"Copyright Â© "}
      <Link color="inherit" href="https://material-ui.com/">
        Your Website
      </Link>{" "}
      {new Date().getFullYear()}
      {"."}
    </Typography>
  );
}

const useStyles = makeStyles((theme) => ({
  paper: {
    marginTop: theme.spacing(8),
    display: "flex",
    flexDirection: "column",
    alignItems: "center",
  },
  avatar: {
    margin: theme.spacing(1),
    backgroundColor: theme.palette.secondary.main,
  },
  form: {
    width: "100%", // Fix IE 11 issue.
    marginTop: theme.spacing(3),
  },
  submit: {
    margin: theme.spacing(3, 0, 2),
  },
}));

export default function SignUp() {
  const classes = useStyles();
  // Form properties
  const NAME = "name";
  const EMAIL = "email";
  const PASSWORD = "password";
  const CONFIRMED_PASSWORD = "confirmedPassword";
  const IS_REMEMBER_ME_TICKED = "isRememberMeTicked";

  const [formState, setFormState] = useState({
    [NAME]: "",
    [EMAIL]: "",
    [PASSWORD]: "",
    [CONFIRMED_PASSWORD]: "",
    [IS_REMEMBER_ME_TICKED]: false,
  });

  const submitForm = (event: React.SyntheticEvent): void => {
    event.preventDefault();
    alert("Form submitted.");
  };

  return (
    <Container component="main" maxWidth="xs">
      <CssBaseline />
      <div className={classes.paper}>
        <Avatar className={classes.avatar}>
          <LockOutlinedIcon />
        </Avatar>
        <Typography component="h1" variant="h5">
          Sign up
        </Typography>
        <form className={classes.form} noValidate onSubmit={submitForm}>
          <Grid container spacing={2}>
            <Grid item xs={12}>
              <TextField
                autoComplete="name"
                name="name"
                variant="outlined"
                required
                fullWidth
                id="name"
                label="Name"
                autoFocus
                onChange={(e) =>
                  setFormState((state) => ({
                    ...state,
                    [NAME]: e.target.value,
                  }))
                }
                value={formState[NAME]}
              />
            </Grid>
            <Grid item xs={12}>
              <TextField
                variant="outlined"
                required
                fullWidth
                id="email"
                label="Email Address"
                name="email"
                autoComplete="email"
                onChange={(e) =>
                  setFormState((state) => ({
                    ...state,
                    [EMAIL]: e.target.value,
                  }))
                }
                value={formState[EMAIL]}
              />
            </Grid>
            <Grid item xs={12}>
              <TextField
                variant="outlined"
                required
                fullWidth
                name="password"
                label="Password"
                type="password"
                id="password"
                autoComplete="current-password"
                onChange={(e) =>
                  setFormState((state) => ({
                    ...state,
                    [PASSWORD]: e.target.value,
                  }))
                }
                value={formState[PASSWORD]}
              />
            </Grid>
            <Grid item xs={12}>
              <TextField
                variant="outlined"
                required
                fullWidth
                name="confirm-password"
                label="Confirm Password"
                type="confirm-password"
                id="confirm-password"
                autoComplete="current-password"
                onChange={(e) =>
                  setFormState((state) => ({
                    ...state,
                    [CONFIRMED_PASSWORD]: e.target.value,
                  }))
                }
                value={formState[CONFIRMED_PASSWORD]}
              />
            </Grid>
            <Grid item xs={12}>
              <FormControlLabel
                control={
                  <Checkbox
                    value="allowExtraEmails"
                    color="primary"
                    checked={formState[IS_REMEMBER_ME_TICKED]}
                    onChange={() =>
                      setFormState((state) => ({
                        ...state,
                        [IS_REMEMBER_ME_TICKED]: !formState[
                          IS_REMEMBER_ME_TICKED
                        ],
                      }))
                    }
                  />
                }
                label="I want to receive inspiration, marketing promotions and updates via email."
              />
            </Grid>
          </Grid>
          <Button
            type="submit"
            fullWidth
            variant="contained"
            color="primary"
            className={classes.submit}
          >
            Sign Up
          </Button>
          <Grid container justify="flex-end">
            <Grid item>
              <Link href="#" variant="body2">
                Already have an account? Sign in
              </Link>
            </Grid>
          </Grid>
        </form>
      </div>
      <Box mt={5}>
        <Copyright />
      </Box>
    </Container>
  );
}
